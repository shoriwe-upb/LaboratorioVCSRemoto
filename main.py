from math import sqrt

def main():
    a = float(input("number a >>"))
    b = float(input("number b >>"))
    c = float(input("number c >>"))
    d = float(input("number d >>"))

    print(f"a * c = {a * c}")
    print(f"2 * a = {2 * a}")
    print(f"b^2 = {b ** 2}")
    print(f"sqrt(d) = {sqrt(d)}")

if __name__ == "__main__":
    main()